class JHLineChart extends StatelessWidget {
  const JHLineChart({required this.model, Key? key}) : super(key: key);
  final ChildModel model;
  @override
  Widget build(BuildContext context) {
    return LineChart(
      sampleData2,
      swapAnimationDuration: const Duration(milliseconds: 250),
    );
  }

  LineChartData get sampleData2 => LineChartData(
        lineTouchData: lineTouchData2,
        gridData: gridData,
        titlesData: titlesData2,
        borderData: borderData,
        lineBarsData: lineBarsData2,
        minX: 0,
        maxX: 14,
        maxY: 6,
        minY: 0,
      );

  FlTitlesData get titlesData1 => FlTitlesData(
        bottomTitles: AxisTitles(
          sideTitles: bottomTitles,
        ),
        rightTitles: AxisTitles(
          sideTitles: SideTitles(showTitles: false),
        ),
        topTitles: AxisTitles(
          sideTitles: SideTitles(showTitles: false),
        ),
        leftTitles: AxisTitles(
          sideTitles: leftTitles(),
        ),
      );

  LineTouchData get lineTouchData2 => LineTouchData(
        enabled: false,
      );

  FlTitlesData get titlesData2 => FlTitlesData(
        bottomTitles: AxisTitles(
          sideTitles: bottomTitles,
        ),
        rightTitles: AxisTitles(
          sideTitles: SideTitles(showTitles: false),
        ),
        topTitles: AxisTitles(
          sideTitles: SideTitles(showTitles: false),
        ),
        leftTitles: AxisTitles(
          sideTitles: leftTitles(),
        ),
      );

  List<LineChartBarData> get lineBarsData2 => [
        lineChartBarData2_1,
        lineChartBarData2_2,
        lineChartBarData2_3,
      ];

  Widget leftTitleWidgets(double value, TitleMeta meta) {
    const style = TextStyle(
      fontWeight: FontWeight.bold,
      fontSize: 14,
    );
    String text;
    switch (value.toInt()) {
      case 1:
        text = '1m';
        break;
      case 2:
        text = '2m';
        break;
      case 3:
        text = '3m';
        break;
      case 4:
        text = '5m';
        break;
      case 5:
        text = '6m';
        break;
      default:
        return Container();
    }

    return Text(text, style: style, textAlign: TextAlign.center);
  }

  SideTitles leftTitles() => SideTitles(
        getTitlesWidget: leftTitleWidgets,
        showTitles: true,
        interval: 1,
        reservedSize: 40,
      );

  Widget bottomTitleWidgets(double value, TitleMeta meta) {
    Widget text;

    if (model.appsUsageModel.isEmpty) {
      text = Text('0');
    }
    switch (value.toInt()) {
      case 2:
        if (model.appsUsageModel.isEmpty) {
          text = Text('0');
        } else {
          text = Image.memory(
            model.appsUsageModel[getRandom(model.appsUsageModel.length - 1)]
                .appIcon!,
            height: 25,
          );
        }
        break;
      case 4:
        if (model.appsUsageModel.isEmpty) {
          text = Text('0');
        } else {
          text = Image.memory(
            model.appsUsageModel[getRandom(model.appsUsageModel.length - 1)]
                .appIcon!,
            height: 25,
          );
        }
        break;
      case 6:
        if (model.appsUsageModel.isEmpty) {
          text = Text('0');
        } else {
          text = Image.memory(
            model.appsUsageModel[getRandom(model.appsUsageModel.length - 1)]
                .appIcon!,
            height: 25,
          );
        }
        break;
      case 8:
        if (model.appsUsageModel.isEmpty) {
          text = Text('0');
        } else {
          text = Image.memory(
            model.appsUsageModel[getRandom(model.appsUsageModel.length - 1)]
                .appIcon!,
            height: 25,
          );
        }
        break;
      case 10:
        if (model.appsUsageModel.isEmpty) {
          text = Text('0');
        } else {
          text = Image.memory(
            model.appsUsageModel[getRandom(model.appsUsageModel.length - 1)]
                .appIcon!,
            height: 25,
          );
        }
        break;
      default:
        text = const Text('');
        break;
    }

    return SideTitleWidget(
      axisSide: meta.axisSide,
      space: 10,
      child: text,
    );
  }

  SideTitles get bottomTitles => SideTitles(
        showTitles: true,
        reservedSize: 32,
        interval: 1,
        getTitlesWidget: bottomTitleWidgets,
      );

  FlGridData get gridData => FlGridData(show: false);

  FlBorderData get borderData => FlBorderData(
        show: true,
        border: Border(
          bottom: BorderSide(
              color: CustomColors.indigoDark.withOpacity(0.2), width: 4),
          left: const BorderSide(color: Colors.transparent),
          right: const BorderSide(color: Colors.transparent),
          top: const BorderSide(color: Colors.transparent),
        ),
      );

